public class Assign146p {

	public static void main(String[] args) {
		int result = 0;
		result += 10;
		
		System.out.println(result);
		result -= 5;
		System.out.println(result);
		result *= 3;
		System.out.println(result);
		result /= 5;
		System.out.println(result);
		result %= 3;
		System.out.println(result);
		
		int su1 = 2;
		int su2 = 2;
		System.out.println(su1 & su2);
		System.out.println(su1 | su2);
		System.out.println(su1 ^ su2);
		
	}

}
